//Write a Java program to print all natural numbers in reverse (from n to 1). -     using while loop
import java.util.Scanner;
class PNNinReverse
{
    public static void main(String args[])
    {
        int n = 5;
        while( n > 0 )
        {
            n--;
            System.out.println("n = " + n );
        }
    }
}